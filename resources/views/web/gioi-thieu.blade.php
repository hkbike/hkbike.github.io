                                            <p class="post-item-firts">Công ty Cổ phần HT BIKE Hà Nội thành lập ngày 30 tháng 6 năm 1960 trực thuộc UBND thành phố Hà Nội, theo giấy phép đăng ký kinh doanh số 0100100424 do Sở Kế hoạch và Đầu tư Thành phố Hà Nội cấp.</p>
                                            <p>Quá trình thành lập:

                                                Công ty Cổ phần HT BIKE Hà Nội (tiền thân là nhà máy xe đạp HT BIKE) được thành lập ngày 30/06/1960.
                                                <br>
                                                Tháng 10/2004, Công ty xe đạp ViHa và Công ty Xe đạp xe máy Đống Đa sáp nhập vào Công ty Xe máy xe đạp HT BIKE theo Quyết định của UBND Thành phố Hà Nội;

                                                Tháng 11/2005, Công ty xe máy xe đạp HT BIKE chuyển thành Công ty TNHH Nhà nước một thành viên Thống Nhất theo Quyết định số 162/2005/QĐ-UB ngày 18/10/2005 của UBND Thành phố Hà Nội;

                                                Tháng 01/2011, Công ty TNHH Nhà nước một thành viên HT BIKE chuyển thành Công ty TNHH một thành viên HT BIKE theo Quyết định số 287/QĐ-TN ngày 18/01/2011 của UBND Thành phố Hà Nội, tổ chức và hoạt động theo mô hình công ty mẹ – công ty con. Công ty hoạt động theo luật pháp quy định và theo Điều lệ đã được UBND thành phố phê duyệt theo Quyết định 6139/QĐ-UBND ngày 30/12/2011;

                                                Ngày 27/02/2017 , Công ty TNHH một thành viên HT BIKE chuyển đổi thành Công ty Cổ phần HT BIKE Hà Nội theo Quyết định số 607/QĐ-UBND ngày 08/02/2017 .
                                                <br>
                                                Trong 57 năm xây dựng và phát triển với thương hiệu vượt thời gian, HT BIKE đã đưa ra thị trường triệu triệu sản phẩm phục vụ cho mọi đối tượng khách hàng với mọi lứa tuổi. “Nghĩ đến xe đạp, Nghĩ về HT BIKE” đã trở thành nếp nghĩ, thói quen và là địa chỉ tin cậy của người tiêu dùng trong nước và quốc tế.
                                                <br>
                                                “Uy tín” và “Chất lượng” là phương châm kinh doanh của Công ty. Trong quá trình sản xuất, Công ty luôn tuân thủ tuyệt đối các quy định về tiêu chuẩn chất lượng, có đăng ký nhãn hiệu hàng hoá và được Cục Sở hữu trí tuệ cấp giấy chứng nhận bảo hộ độc quyền. “Lấy chất lượng làm hàng đầu”, Công ty Cổ phần HT BIKE Hà Nội luôn cung cấp các sản phẩm, dịch vụ đáp ứng yêu cầu của khách hàng và phù hợp với các quy định về tiêu chuẩn của sản phẩm, dịch vụ; góp phần bảo vệ môi trường, vì sức khỏe cộng đồng và vì quyền lợi của tất cả các bên có liên quan.
                                                <br>
                                                Hiệu quả sản xuất kinh doanh chính là thước đo giá trị quan trọng về tăng trưởng kinh tế và chỗ dựa cơ bản để đánh giá việc thực hiện các mục tiêu kinh tế xã hội của doanh nghiệp. Hàng năm, Công ty luôn hoàn thành nhiệm vụ kinh tế xã hội được giao, bảo toàn và phát triển vốn Nhà nước, tăng tích luỹ. Thực hiện tốt các quy định về tài chính, kế toán và luôn hoàn thành nghĩa vụ nộp ngân sách Nhà nước.</p>
